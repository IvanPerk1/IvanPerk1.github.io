function setLanguage(lang) {
    if (!translations[lang]) lang = 'en';
    document.documentElement.lang = lang;
    document.documentElement.dir = (lang === 'ar' || lang === 'fa') ? 'rtl' : 'ltr';

    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (key === 'hero-title') {
                element.innerHTML = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });

    // Sync both dropdowns
    const desktop = document.getElementById('lang-select-desktop');
    const mobile = document.getElementById('lang-select-mobile');
    if (desktop) desktop.value = lang;
    if (mobile) mobile.value = lang;

    localStorage.setItem('wakePcLang', lang);
}

// Auto-detect language on load
document.addEventListener('DOMContentLoaded', () => {
    let lang = localStorage.getItem('wakePcLang');
    if (!lang) {
        const browserLang = (navigator.language || '').split('-')[0].toLowerCase();
        lang = translations[browserLang] ? browserLang : 'en';
    }
    if (!translations[lang]) lang = 'en';
    setLanguage(lang);
});
